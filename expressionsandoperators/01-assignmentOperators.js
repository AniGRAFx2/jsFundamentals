/*
ASSIGNMENT OPERATORS
*/

let r = 5
//the "=" is the Assignment
//Addition Assn
r += 1; //r = r+ or, 5 + 1 in ths case.
console.log(r);


//SUBTRACTION ASSN
r -= 2; // r = r - 2
console.log(r);

//MULTIPLICATION ASSN
r *= 2; //r = r / 2
console.log(r);

//DIVISION ASSN
r /= 1;  // r = r/1
console.log(r);


//EXPONENTIAL ASSN
r **= 3; // r = r * r
console.log(r)

//REMAINDER (Modulus) ASSN
r %= 1; // r = r % 1
console.log(r);

